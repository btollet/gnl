#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	size_t	i;

	if (!s1 || !s2)
		return (NULL);
	i = 0;
	if ((result = (char *)malloc(sizeof(char) * ft_strlen(s1)
		+ ft_strlen(s2) + 1)) == NULL)
		return (NULL);
	while (*s1)
		result[i++] = *s1++;
	while (*s2)
		result[i++] = *s2++;
	result[i] = '\0';
	return (result);
}
